		LibItemLink_Data = { }
		LibItemLink_Data["itemQuality"] = { }

		local data = LibItemLink_Data["itemQuality"]

		for subType = 0, 400 do
			local itemLink = GetItemLink(subType)
			local itemId = GetItemLinkItemId(itemLink)
			if itemId and itemId >= 0 then
				local quality = GetItemLinkQuality(itemLink)
				local requiredLevel = GetItemLinkRequiredLevel(itemLink)
				local requiredChampionPoints = GetItemLinkRequiredChampionPoints(itemLink)
				if requiredChampionPoints > 0 then
					requiredLevel = MAX_LEVEL + math.floor(requiredChampionPoints / 10)
				end

				data[#data + 1] = string.format("[%d] = { level = %d, quality = %d },", subType, requiredLevel, quality)
			end
		end
